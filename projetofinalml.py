def clean_plate_text(text):
    if text is None:
        return ""
    text = text.upper()
    # Remover s√≠mbolos comuns
    for ch in ['#', '-', ' ', ',', '.', '(', ')', '[', ']', '_', ':']:
        text = text.replace(ch, '')
    # Substituir caracteres confusos (exemplo)
    substitutions = {
        '0': 'O',  # s√≥ se fizer sentido no contexto da placa
        '1': 'I',
        '5': 'S',
        '8': 'B',
        # ajuste conforme padr√£o da sua base
    }
    for k, v in substitutions.items():
        text = text.replace(k, v)
    # Manter s√≥ letras e n√∫meros
    text = ''.join(c for c in text if c.isalnum())
    return text

import re

