from collections import Counter

def character_confusion_matrix(gt_list, pred_list):
    confusion = Counter()

    for gt, pred in zip(gt_list, pred_list):
        # Usar o tamanho m√≠nimo para comparar
        length = min(len(gt), len(pred))
        for i in range(length):
            if gt[i] != pred[i]:
                confusion[(gt[i], pred[i])] += 1
        # Caso gt maior que pred, considerar erros de omiss√£o
        for c in gt[length:]:
            confusion[(c, None)] += 1
        # Caso pred maior que gt, considerar erros de inser√ß√£o
        for c in pred[length:]:
            confusion[(None, c)] += 1
    return confusion

