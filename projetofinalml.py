import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from collections import defaultdict

def build_confusion_matrix_chars(gt_list, pred_list):
    chars = set()
    # Primeiro, pegar todos os caracteres √∫nicos do conjunto
    for text in gt_list + pred_list:
        for c in text:
            chars.add(c)
    chars = sorted(chars)

    # Mapeamento char -> √≠ndice
    char_to_idx = {c: i for i, c in enumerate(chars)}

    # Inicializar matriz de confus√£o
    matrix = np.zeros((len(chars), len(chars)), dtype=int)

    for gt, pred in zip(gt_list, pred_list):
        length = min(len(gt), len(pred))
        for i in range(length):
            gt_c = gt[i]
            pred_c = pred[i]
            matrix[char_to_idx[gt_c], char_to_idx[pred_c]] += 1
        # Considerar erros de omiss√£o (char esperado, mas n√£o previsto)
        for c in gt[length:]:
            matrix[char_to_idx[c], :] += 1  # conta como erro geral (pode adaptar)
        # Considerar erros de inser√ß√£o (char previsto, mas n√£o esperado)
        for c in pred[length:]:
            matrix[:, char_to_idx[c]] += 1  # conta como erro geral (pode adaptar)

    return matrix, chars

